# if else-if else condition statement
Grade = input("what are your grades")
if Grade == "A":
    print(f'Your grade is {5.0}')
elif Grade == "B":
    print(f'Your Grade is {4.0}')
elif Grade == "C":
    print(f'Your grade is {3.0}')
elif Grade == "D":
    print(f'Your grade is {2.0}')
elif Grade == "F":
    print(f'Your grade is {0.0}')
else:
    print("you failed")
